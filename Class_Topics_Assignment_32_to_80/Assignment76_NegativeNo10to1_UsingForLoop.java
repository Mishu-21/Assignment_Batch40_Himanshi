//To print Negative numbers from -10 to -1  using FOR loop
package Class_Topics_Assignment;

public class Assignment76_NegativeNo10to1_UsingForLoop {

	
	public static void main(String[] args) throws InterruptedException {
		
		 for(int i=-10;i<=-1;i++)
		{
			
				 System.out.println(i);
					Thread.sleep(1000);
			
			
		}
	}
}
