//A program with muliple IF statement

package Class_Topics_Assignment;

public class Assignment41_MultipleIfStatement {

	public static void main(String[] args) {
	int age=21;
	if(age>18)
	{
		System.out.println("Greater than 18");
	}
	
	if(age>20)
	{
		System.out.println("Greater than 20");
	}
	
	if(age>21)
	{
		System.out.println("Greater than 21");
	}
	
	if(age>19)
	{
		System.out.println("Greater than 19");
	}
	
	if(age>22)
	{
		System.out.println("Greater than 22");
	}
}
	
		
}
