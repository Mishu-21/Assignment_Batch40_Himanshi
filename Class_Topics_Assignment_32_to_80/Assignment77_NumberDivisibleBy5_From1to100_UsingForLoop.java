// To print the numbers divisible by 5 from 1 to 100
package Class_Topics_Assignment;

public class Assignment77_NumberDivisibleBy5_From1to100_UsingForLoop {

	public static void main(String[] args) throws InterruptedException {
		for(int i=1;i<=100;i++)
		{
			 if(i%5==0)
			 {
				 System.out.println(i);
					Thread.sleep(1000);
			 }
			
		}
	}
	}
	 

